a_comp=3+3j  #복소수형 변수 선언
print(a_comp)
print(type(a_comp)) #변수형 유형 확인

b_list=[1,2,3,4,5]  #리스트 : 순서가 있는 데이터 모음(집합)
print(b_list)
type(b_list) #변수형 유형 확인

c_tu=(1,2,3,4,5)  #튜플 : 수정할 수 없는 데이터 모음(집합)
print(c_tu)
type(c_tu) #변수형 유형 확인

d_dic={1:"c++", 2:"java"}  #딕셔너리 : 키와 값을 하나의 쌍으로 이룬 데이터 모음(집합)
print(d_dic)
type(d_dic) #변수형 유형 확인

e_set={1,2,3,4,5,1,2,6}  #세트: 중복을 허용하지 않는 데이터 모음(집합)
print(e_set)
type(e_set) #변수형 유형 확인
